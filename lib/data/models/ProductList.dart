// data/models/ProductList.dart
import 'Product.dart';

class ProductList {
  final List<Product> products;
  final int total;
  final int skip;
  final int limit;

  ProductList({
    required this.products,
    required this.total,
    required this.skip,
    required this.limit,
  });

  factory ProductList.fromJson(Map<String, dynamic> json) {
    return ProductList(
      products: (json['products'] as List?)
          ?.map((i) => Product.fromJson(i))
          .toList() ??
          [],
      total: json['total'] ?? 0,
      skip: json['skip'] ?? 0,
      limit: json['limit'] ?? 0,
    );
  }
}
